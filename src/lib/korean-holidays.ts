const DAY_NAMES = ['일요일', '월요일', '화요일', '수요일', '목요일', '금요일', '토요일'];

export type DateWarning = {
    type: 'holiday' | 'saturday' | 'sunday';
    message: string;
};

export type DateInfo = {
    type: 'business' | 'holiday' | 'saturday' | 'sunday';
    message: string;
    isWarning: boolean;
};

/**
 * ISO 날짜 문자열(YYYY-MM-DD)에 대해 주말/공휴일 경고를 반환.
 * 문제 없으면 null.
 */
export function getDateWarning(iso: string): DateWarning | null {
    const info = getDateInfo(iso);
    if (!info || !info.isWarning) return null;
    return { type: info.type as DateWarning['type'], message: info.message };
}

export function getDateInfo(iso: string): DateInfo | null {
    if (!iso || !/^\d{4}-\d{2}-\d{2}$/.test(iso)) return null;

    // 공휴일 테이블 우선 확인 (음력 기반 + 연도별 특수일)
    const lunarName = LUNAR_HOLIDAYS[iso];
    if (lunarName) {
        const d = new Date(`${iso}T00:00:00`);
        const dayName = DAY_NAMES[d.getDay()];
        return { type: 'holiday', message: `⚠️ ${iso}은 ${lunarName}입니다 (${dayName})`, isWarning: true };
    }

    // 고정 공휴일 확인
    const mmdd = iso.slice(5); // MM-DD
    const fixedName = FIXED_HOLIDAYS[mmdd];
    if (fixedName) {
        const d = new Date(`${iso}T00:00:00`);
        const dayName = DAY_NAMES[d.getDay()];
        return { type: 'holiday', message: `⚠️ ${iso}은 ${fixedName}입니다 (${dayName})`, isWarning: true };
    }

    // 주말 확인
    const d = new Date(`${iso}T00:00:00`);
    const dow = d.getDay();
    if (dow === 6) return { type: 'saturday', message: `⚠️ ${iso}은 토요일입니다`, isWarning: true };
    if (dow === 0) return { type: 'sunday', message: `⚠️ ${iso}은 일요일입니다`, isWarning: true };

    return { type: 'business', message: `${DAY_NAMES[dow]}입니다`, isWarning: false };
}
